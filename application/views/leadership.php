		           Mr. G V Selvam was born to Smt. Rajeswari Ammal & Thiru. G. Viswanathan, Founder of VIT University, formerly Vellore Engineering College, on 26 October 1973. He holds an MBA from the University of Madras. Mr.Selvam assumed charge as Vice Chairman of Vellore Engineering College in 1999, and worked towards elevating the institution to a University. Following the footsteps of his illustrious father, Mr.Selvam spearheaded the evolution of the erstwhile Vellore Engineering College to VIT University, an institution of international repute. He assumed charge as the first Pro Chancellor of VIT University. This bestowed on him the rare honour of being the Youngest Pro-Chancellor in the world as cited in the Limca Book of Records, 2005. He was the Vice President of the VIT Chennai Campus in its formative years, from 2010 to 2013, when he built the campus from scratch. The high rise structures, the lush green atmosphere and the ultra-modern infrastructure that adorn the Chennai Campus were created within a short span of time, revealing his exceptional organizational and leadership abilities. He has taken painstaking efforts to develop the VIT Chennai campus in conformity with the requirements of the Green LEED certification. After his successful stint as Vice President of Chennai Campus, he is currently serving as the Vice President (Administration).
